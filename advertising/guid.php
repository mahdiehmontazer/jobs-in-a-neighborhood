 <p>کاربر گرامی ورود شما را به شهر اینترنتی قم خوش آمد می گوییم</p>
 <p>
 اطلاع می رسانیم این سایت به منظور سهولت و آسایش همشهریان عزیز طراحی شده است است و هدف  سهولت دسترسی به مراکز
فروشگاه ها و آگهی های تبلیغاتی و مشاغل شما عزیزان می باشد با عضویت در سایت می توانید مهارت ها و مشاغل و آگهی ها ی خود را به همشهریان اعلام نمایید

 </p>
