<p class="guid">شما می توانید به روش زیر مبلغ مورد نظر برای درج آگهی  خود را به حساب سایت اینترنتی قم  واریز نمایید.</p>
<h3>پرداخت آنلاین با استفاده از کارتهای بانکی عضو شتاب</3>
<p>امکان پرداخت وجه آگهی  بطور آنلاین و با استفاده از کارتهای عضو شتاب فراهم می باشد.
بعد از اینکه در سایت ثبت نام نمودید و با نام کاربری و رمز عبور خود وارد شدید ، جهت درج آگهی ویژه ابتدا به صفحه درج آگهی بروید و اطلاعات آگهی را تکمیل نمایید .
سپس روی دکمه درج آکهی برای تایید و پرداخت اینترنتی کلیک نمایید.
لازم به ذکر است که برای فعال شدن خرید اینترنتی کارت بانکی خود ، می بایست از طریق دستگاههای خود پرداخت رمز دوم کارت را فعال نموده باشید. </p>
<h3>ATMتوسط  دستگاه های خودپرداز </h3>
<p>چنانچه دارای یکی از  کارتهای بانکی عضو  شتاب هستید با مراجعه به یکی از دستگاه های خودپرداز (ATM ) بانک خود  می توانید در تمامی اوقات شبانه روز وجه آگهی  را پرداخت نمایید. بانکهای   صادرات  - ملی  - پارسیان ( با کارت نقدی - هدیه - اینترنت) - اقتصاد نوین - کارآفرین - ملت - کشاورزی  - تجارت و پاسارگاد  عضو شتاب هستند.  

برای واریز به حساب شهر اینترنتی قم به یکی از نزدیکترین دستگاه های خودپرداز بانک مرتبط با کارت خود و یا تمامی شعب بانکهای صادرات یا ملی مراجعه و عملیات زیر را انجام دهید:</p>
<p>
ابتدا کارت خود را وارد و رمز عبور را وارد نمایید
سپس از منوی موجود گزینه "انتقال وجه" را انتخاب نمایید
سپس گزینه "به کارت دیگر" را انتخاب کنید
حالا لازم است تا مبلغ مورد نظر را به ریال وارد نمایید. مثلا اگر می خواهید آگهی 2ماهه در سایت قراردهید مبلغ 150,000  ریال را وارد نمایید و 24 ماهه 250,000  ریال و ... و سپس کلید ورود را بزنید
سپس  لازم است تا یکی از شماره کارت های اختصاصی سایت اینترنتی قم را وارد نمایید: 
</p>
