    راهنمای استفاده از سایت
    </p>
    <p>
    .
سایت به صورتی طراحی شده است که شما می توانید به راحتی وبا چند قدم آگهی های خود را در سایت قرار دهید.فقط از شما عزیزان خواهشمندیم ضمن مطالعه قوانین و راهنمای سایت ما را در این هدف یاری رسانید

    </p>
    <p>1.      
کاربر گرامی استفاده از سایت همینطور که مشاهده کردید بسیار راحت می باشد.
برای مشاهده آگهی ها نیاز به عضویت نیست.
برای درج مشاغل و آگهی های خود بایستی ابتدا عضو سایت شوید .
</p>
<p>2.      سپس با شماره کاربری و رمز عبور خود وارد پانل مخصوص خود می شوید</p>
<p>3.    و می توانید آگهی جدید درج نمایید و حتی آگهی های گذشته خود را مشاهده ویرایش و حذف نمایید.
برای درج آگهی خود روی لینک درج آگهی کلیک کنید فرم درج اگهی ظاهر می شود آنرا با دقت پر نمایید .
</p>
<p>
 در صورتی که آگهی شما عکس دارد در دو سایز عکس خود را می توانید قرار دهید.
 </p>
 <p>
 1)سایز 300*600 برای  عکس درون اسلاید صفحه اصلی(هزینه 10000 تومان) 
 </p>
 <p>
2)عکس با سایز 150*150  برای عکس کنار آگهی خود
</p>
<p>
در صورت تمایل می توانید آگهی خود را بدون عکس قرار دهید. 
</p>
